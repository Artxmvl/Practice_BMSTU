package org.example;

// Класс, в котором решается задача поиска максимума из четырёх чисел
public class zadacha2_fork {
    // Статический метод, который принимает 4 целых числа и возвращает максимальное
    public static int findMax(int a, int b, int c, int d) {
        // Считаем, что сначала максимум — это первое число a
        int max = a;

        // Если b больше текущего максимума, обновляем max
        if (b > max) {
            max = b;
        }
        // Если c больше текущего максимума, обновляем max
        if (c > max) {
            max = c;
        }
        // Если d больше текущего максимума, обновляем max
        if (d > max) {
            max = d;
        }

        // Возвращаем найденный максимум
        return max;
    }



    public static void main(String[] args) {
        // Объявляем 4 числа, среди которых будем искать максимум
        int a = 45, b = 96, c = 3, d = 54;

        // Вызываем метод findMax и сохраняем результат в переменную max
        int max = findMax(a, b, c, d);

        // Выводим результат на экран
        System.out.println("Максимальное число: " + max);
    }
}

